//break
for (let i = 1; i <= 5; i++) {
    if (i === 3) {
        break
    }
    console.log(i);
}
//o break termina o laço for

//continue
for (let i = 1; i <= 5; i++) {
    if(i === 3){
        continue
    }
    console.log(i);
}
//o continue não sai do laço for, ele apenas
//pula quando for falso

//alt + shift + para baixo ou para cima duplica